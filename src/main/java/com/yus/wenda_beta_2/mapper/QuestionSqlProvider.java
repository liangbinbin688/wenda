    public String countByExample(QuestionExample example) {
        BEGIN();
        SELECT("count(*)");
        FROM("question");
        applyWhere(example, false);
        return SQL();
    }

    public String deleteByExample(QuestionExample example) {
        BEGIN();
        DELETE_FROM("question");
        applyWhere(example, false);
        return SQL();
    }

    public String insertSelective(Question record) {
        BEGIN();
        INSERT_INTO("question");
        
        if (record.getId() != null) {
            VALUES("id", "#{id,jdbcType=INTEGER}");
        }
        
        if (record.getTitle() != null) {
            VALUES("title", "#{title,jdbcType=VARCHAR}");
        }
        
        if (record.getUserId() != null) {
            VALUES("user_id", "#{userId,jdbcType=INTEGER}");
        }
        
        if (record.getCreatedDate() != null) {
            VALUES("created_date", "#{createdDate,jdbcType=TIMESTAMP}");
        }
        
        if (record.getCommentCount() != null) {
            VALUES("comment_count", "#{commentCount,jdbcType=INTEGER}");
        }
        
        if (record.getContent() != null) {
            VALUES("content", "#{content,jdbcType=LONGVARCHAR}");
        }
        
        return SQL();
    }

    public String selectByExampleWithBLOBs(QuestionExample example) {
        BEGIN();
        if (example != null && example.isDistinct()) {
            SELECT_DISTINCT("id");
        } else {
            SELECT("id");
        }
        SELECT("title");
        SELECT("user_id");
        SELECT("created_date");
        SELECT("comment_count");
        SELECT("content");
        FROM("question");
        applyWhere(example, false);
        
        if (example != null && example.getOrderByClause() != null) {
            ORDER_BY(example.getOrderByClause());
        }
        
        return SQL();
    }

